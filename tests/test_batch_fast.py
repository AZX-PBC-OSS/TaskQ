@pytest.mark.integration
class TestTI7TransactionalBatchFast:
    """Transactional batch_fast — rollback means no rows."""

    async def test_rollback_means_no_rows(self, pg_dsn: str) -> None:
        import asyncpg

        from taskq import TaskQ
        from taskq.migrate import apply_pending

        schema = "taskq_test_batch_fast_ti7"
        conn = await asyncpg.connect(pg_dsn)
        try:
            await conn.execute(f'DROP SCHEMA IF EXISTS "{schema}" CASCADE')
            await apply_pending(conn, schema=schema)
        finally:
            await conn.close()

        batch_id = uuid4()
        async with TaskQ(dsn=pg_dsn, schema=schema) as tq:
            tx_conn = await asyncpg.connect(pg_dsn)
            tr = tx_conn.transaction()
            await tr.start()
            try:
                await tq.enqueue_batch_fast(
                    [_make_item(i) for i in range(3)],
                    batch_id=batch_id,
                    connection=tx_conn,
                )
            finally:
                await tr.rollback()
                await tx_conn.close()

        conn = await asyncpg.connect(pg_dsn)
        try:
            count = await conn.fetchval(f'SELECT count(*) FROM "{schema}".jobs')  # noqa: S608
            assert count == 0
        finally:
            await conn.close()


