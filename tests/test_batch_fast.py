@pytest.mark.integration
class TestTIPastScheduledAtNormalized:
    """Past scheduled_at is normalized to now() — matches enqueue/enqueue_batch.

    Regression: enqueue_batch_fast stored the stale past timestamp instead
    of normalizing it to the batch clock like the regular enqueue path
    (which uses ``COALESCE($N, now())``).
    """

    async def test_past_scheduled_at_not_stored_stale(self, pg_dsn: str) -> None:
        import asyncpg

        from taskq import TaskQ
        from taskq._json import dumps_str
        from taskq.migrate import apply_pending

        schema = "taskq_test_batch_fast_past_scheduled"
        conn = await asyncpg.connect(pg_dsn)
        try:
            await conn.execute(f'DROP SCHEMA IF EXISTS "{schema}" CASCADE')
            await apply_pending(conn, schema=schema)
        finally:
            await conn.close()

        # A scheduled_at well in the past relative to the live PG clock.
        past = _START
        batch_id = uuid4()
        async with TaskQ(dsn=pg_dsn, schema=schema) as tq:
            count = await tq.enqueue_batch_fast(
                [EnqueueItem(actor_ref=_test_actor, payload=_Payload(value=1), scheduled_at=past)],
                batch_id=batch_id,
            )

        assert count == 1

        conn = await asyncpg.connect(pg_dsn)
        try:
            rec = await conn.fetchrow(
                f'SELECT status, scheduled_at FROM "{schema}".jobs '  # noqa: S608
                f"WHERE metadata @> $1::jsonb",
                dumps_str({"batch_id": str(batch_id)}),
            )
            assert rec is not None
            assert rec["status"] == "pending"
            assert rec["scheduled_at"] != past
        finally:
            await conn.close()
