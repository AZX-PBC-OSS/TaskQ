@pytest.mark.integration
class TestTI5PerformanceComparison:
    """COPY FROM faster than UNNEST for 1K items."""

    @pytest.mark.slow  # perf comparison, not correctness; relaxed multiplier under load
    async def test_copy_faster_than_unnest_1k(self, pg_dsn: str) -> None:
        """For 1K items (max for enqueue_batch), COPY FROM must be faster than UNNEST."""
        import asyncpg

        from taskq import TaskQ
        from taskq._json import dumps_str
        from taskq.migrate import apply_pending

        schema = "taskq_test_batch_fast_ti5"
        conn = await asyncpg.connect(pg_dsn)
        try:
            await conn.execute(f'DROP SCHEMA IF EXISTS "{schema}" CASCADE')
            await apply_pending(conn, schema=schema)
        finally:
            await conn.close()

        items_1k = [_make_item(i % 1000) for i in range(1_000)]

        async with TaskQ(dsn=pg_dsn, schema=schema) as tq:
            # Time COPY FROM
            batch_id_fast = uuid4()
            start = time.monotonic()
            count_fast = await tq.enqueue_batch_fast(items_1k, batch_id=batch_id_fast)
            elapsed_fast = time.monotonic() - start
            assert count_fast == 1_000

            # Clean up inserted rows
            conn = await asyncpg.connect(pg_dsn)
            try:
                await conn.execute(
                    f'DELETE FROM "{schema}".jobs WHERE metadata @> $1::jsonb',  # noqa: S608
                    dumps_str({"batch_id": str(batch_id_fast)}),
                )
            finally:
                await conn.close()

            # Time UNNEST (regular enqueue_batch)
            batch_id_unnest = uuid4()
            start = time.monotonic()
            handle = await tq.enqueue_batch(items_1k, batch_id=batch_id_unnest)
            elapsed_unnest = time.monotonic() - start
            assert handle.size == 1_000

        assert elapsed_fast <= elapsed_unnest * 20, (
            f"COPY FROM ({elapsed_fast:.3f}s) should be competitive with "
            f"UNNEST ({elapsed_unnest:.3f}s) for 1K items"
        )


